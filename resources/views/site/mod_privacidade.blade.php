3. Compartilhamento de dados e sua privacidade
Como já falamos no início deste documento, nós não comercializamos ou cedemos seus dados pessoais ou de navegação a terceiros, exceto:

(I) Com a empresa parceira, Zyoncore Sistemas Ltda., que fornece o sistema para envio de imagens pela web, diagramação de fotolivros, venda e montagem de produtos personalizados, entre outros;
(II) Com possíveis filiais da nossa empresa, que compartilham a mesma base de clientes, estratégias de marketing e gerenciamento;
(III) Com entidades governamentais, com autorização específica, visando o cumprimento de suas competências legais, de acordo com a Lei Geral de Proteção de Dados Pessoais (LGPD, Lei nº 13.709/2018);

Como observamos no tópico 2 deste documento, “Como os dados são utilizados”, seus dados são compartilhados com nossa empresa e com a Zyoncore Sistemas Ltda., a título de melhorar todos os serviços que oferecemos a você, viabilizar a compra de produtos em nossas plataformas, entre outros. Buscamos sempre utilizar de forma responsável os dados que coletamos e sempre observando seus direitos descritos na Lei Geral de Proteção de Dados Pessoais (LGPD, Lei nº 13.709/2018).

Sobre o tempo de armazenamento dos dados
Estes dados ficam disponíveis para nossa consulta e são armazenados por tempo indeterminado, ou até que você realize a exclusão da sua conta ou modificação dos seus dados em nossa plataforma. O armazenamento é realizado pela nossa empresa parceira, Zyoncore Sistemas Ltda., em plataforma segura, que atende as medidas de boas práticas, sigilo de dados e segurança previstas na Lei Geral de Proteção de Dados Pessoais (LGPD, Lei nº 13.709/2018)

